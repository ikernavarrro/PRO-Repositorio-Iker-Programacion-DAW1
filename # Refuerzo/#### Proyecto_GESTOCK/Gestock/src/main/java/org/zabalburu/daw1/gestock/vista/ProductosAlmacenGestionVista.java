    private void seleccionarFila(int i) {
        if (i >= 0 && i < tblProductos.getRowCount()) {
            tblProductos.setRowSelectionInterval(i, i);
            tblProductos.scrollRectToVisible(tblProductos.getCellRect(i, 0, true));
        }
    }

    
