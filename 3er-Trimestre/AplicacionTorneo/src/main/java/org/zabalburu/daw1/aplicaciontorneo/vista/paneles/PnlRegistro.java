    private void cargarJuegos() {
        for (Juego juego : servicio.getJuegos()) {
            cbxJuego.addItem(juego);
        }
    }

    private void cargarJugadores() {
        for (Jugador jugador : servicio.getJugadores()) {
            cbxGanador.addItem(jugador);
            cbxPerdedor.addItem(jugador);
        }
    }

    private void inicializarCombos() {
        cbxJuego.setRenderer(new DefaultListCellRenderer() {
            @Override
            public Component getListCellRendererComponent(JList<?> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                JLabel lbl = (JLabel) super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/OverriddenMethodBody
                Juego j = (Juego) value;
                lbl.setText(j.getTitulo());
                return lbl;
            }

        });

        ListCellRenderer r = new DefaultListCellRenderer() {
            @Override
            public Component getListCellRendererComponent(JList<?> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                JLabel lbl = (JLabel) super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/OverriddenMethodBody
                Jugador j = (Jugador) value;
                lbl.setText(j.getNick());
                lbl.setToolTipText("%s, %s [id:%d]".formatted(j.getApellidos(), j.getNombre(), j.getId()));
                lbl.setIcon(j.getImagenAvatar());
                return lbl;
            }
        };

        cbxGanador.setRenderer(r);
        cbxPerdedor.setRenderer(r);
    }

    public void inicializarDatos() {
        cbxJuego.setSelectedIndex(0);
        cbxGanador.setSelectedIndex(0);
        cbxPerdedor.setSelectedIndex(0);
        datePicker.setDateTimeStrict(LocalDateTime.now());
        spiPuntos.setValue(5);
        txtDuracion.setText("");
    }
