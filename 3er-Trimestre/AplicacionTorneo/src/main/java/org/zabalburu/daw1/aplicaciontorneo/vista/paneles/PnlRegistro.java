    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Jugador ganador = (Jugador) cbxGanador.getSelectedItem();
        Jugador perdedor = (Jugador) cbxPerdedor.getSelectedItem();
        if (ganador.equals(perdedor)){
            JOptionPane.showMessageDialog(null, "Debe seleccionar jugadores diferentes!", "Selección", JOptionPane.ERROR_MESSAGE);
        } else {
            Juego j = (Juego) cbxJuego.getSelectedItem();
            Partida p = new Partida();
            p.setJuego(j);
            p.setGana(ganador);
            p.setPierde(perdedor);
            p.setFecha(datePicker.getDateTimeStrict());
            p.setDuracion(txtDuracion.getText());
            p.setPuntos((Integer) spiPuntos.getValue());
            servicio.addPartida(p);
            JOptionPane.showMessageDialog(null, "partida registrada con éxito", "Partida Guardada", JOptionPane.INFORMATION_MESSAGE);
            inicializarDatos();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


