    private void actualizarContadorTotalCostes(List<Revision> revisionesOrdenadas) {
        Double total = 0.0;
        for (Revision r : revisionesOrdenadas) {
            total += r.getCoste();
        }
        ftxTotalCoste.setValue(total);
    }
