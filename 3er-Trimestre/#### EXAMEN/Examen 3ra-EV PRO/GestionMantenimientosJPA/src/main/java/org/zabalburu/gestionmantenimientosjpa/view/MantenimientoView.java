    private void btnAñadirRevisiónActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirRevisiónActionPerformed
        if (!txtDescripcion.getText().isBlank()) {
            Revision r = new Revision();

            /*1*/
            r.setMaquina((Maquina) cbxMaquinas.getSelectedItem());

            /*2*/
            if (!ftxFecha.getText().isBlank()) {
                r.setFecha(DateConversion.fromDate((Date) ftxFecha.getValue()));
            } else {
                r.setFecha(LocalDate.now());
            }

            /*3*/
            r.setDescripcion(txtDescripcion.getText());

            /*4*/
            if (!ftxCoste.getText().isBlank()) {
                r.setCoste(Double.valueOf((ftxCoste.getText().replace(",", "."))));
            } else {
                r.setCoste(0.0);
            }

            try {
                service.nuevaRevision(r);
                JOptionPane.showMessageDialog(this, "Revisión registrada correctamente!", "Información", JOptionPane.INFORMATION_MESSAGE);
                cargarTabla();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "El campo descripción está vacío!", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAñadirRevisiónActionPerformed

    private void btnEliminarRevisiónActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarRevisiónActionPerformed

        if (tblDatos.getSelectedRow() != -1) {
            Integer idRevisionSeleccionada = (Integer) tblDatos.getModel().getValueAt(tblDatos.getSelectedRow(), 0);
            if (JOptionPane.showConfirmDialog(this, "¿Está seguro que desea eliminar la revisión con [ ID : %d ] ?".formatted(idRevisionSeleccionada), "Aviso", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                service.eliminarRevision(idRevisionSeleccionada);
                JOptionPane.showMessageDialog(this, "Revisión eliminada correctamente!", "Información", JOptionPane.INFORMATION_MESSAGE);
                cargarTabla();
            }
        } else {
            JOptionPane.showMessageDialog(this, "No hay ninguna revisión seleccionada para eliminar!", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarRevisiónActionPerformed

