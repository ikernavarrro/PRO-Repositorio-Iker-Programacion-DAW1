    private void inicializarComboTecnicos() {
        cbxTecnicos.removeAllItems();
        for (Tecnico t : service.getTecnicos()) {
            cbxTecnicos.addItem(t);
        }
        actualizarDatos();
        cargarTabla();
    }

    private void actualizarDatos() {
        Tecnico t = (Tecnico) cbxTecnicos.getSelectedItem();
        if (t != null) {
            t = service.getTecnicoConMaquinas(t.getId());
            cargarComboMaquinas(t);
        }
    }

    private void cargarComboMaquinas(Tecnico t) {
        cbxMaquinas.removeAllItems();
        for (Maquina m : t.getMaquinas()
                .stream()
                .sorted((m1, m2) -> m1.getNombre().compareTo(m2.getNombre()))
                .toList()) {
            cbxMaquinas.addItem(m);
        }
    }

    private void cargarTabla() {
        Maquina m = (Maquina) cbxMaquinas.getSelectedItem();
        if (m != null) {

            m = service.getMaquina(m.getId());
            List<Revision> revisionesOrdenadas = m.getRevisiones().stream()
                    .sorted((r1, r2) -> r2.getFecha().compareTo(r1.getFecha()))
                    .toList();

            DefaultTableModel dtm = (DefaultTableModel) tblDatos.getModel();
            dtm.setRowCount(0);

            for (Revision r : revisionesOrdenadas) {
                Vector vctFila = new Vector();
                vctFila.add(r.getId());
                vctFila.add(r.getFecha());
                vctFila.add(r.getDescripcion());
                vctFila.add(r.getCoste());
                dtm.addRow(vctFila);
            }
            actualizarContadorTotalCostes(revisionesOrdenadas);
        }
    }

