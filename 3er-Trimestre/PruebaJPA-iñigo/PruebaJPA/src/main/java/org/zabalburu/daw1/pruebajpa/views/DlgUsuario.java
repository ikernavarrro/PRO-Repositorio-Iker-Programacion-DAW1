    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        boolean alta = (u == null);
        if (alta){
            u = new Usuario();
            u.setHash(new String(pwdPassword.getPassword()));
        }
        u.setNombre(txtNombre.getText());
        u.setApellidos(txtApellidos.getText());
        u.setEmail(txtEmail.getText());
        Date d = (Date) ftxFechaAlta.getValue();
        u.setFechaAlta(d.toInstant()
                      .atZone(ZoneId.systemDefault())
                      .toLocalDate());
        Number n = (Number) ftxSueldo.getValue();
        u.setSueldo(n.doubleValue());
        if (alta){
            servicio.nuevoUsuario(u);
        } else {
            servicio.modificarUsuario(u);
        }
        JOptionPane.showMessageDialog(this,
            "Datos guardados correctamente!");
    }//GEN-LAST:event_btnGuardarActionPerformed

