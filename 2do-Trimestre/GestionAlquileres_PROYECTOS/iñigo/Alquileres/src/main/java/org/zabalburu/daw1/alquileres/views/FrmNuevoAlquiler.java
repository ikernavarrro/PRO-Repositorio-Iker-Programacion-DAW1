    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        Alquiler a = new Alquiler();
        Cliente c = (Cliente) cbxCliente.getSelectedItem();
        a.setCliente(c);
        a.setCoche(coche);
        a.setCosteDia(coche.getCosteDia());
        Number n = (Number) ftxDias.getValue();
        a.setDias(n.intValue());
        Date d = (Date) ftxFecha.getValue();
        a.setFechaAlquiler(DateConversion.fromDate(d));
        a.setFechaDevolucion(null);
        service.nuevoAlquiler(a);
        JOptionPane.showMessageDialog(this, 
            "Alquiler registrado con Éxito!");
        this.dispose();
    }//GEN-LAST:event_btnNuevoActionPerformed


