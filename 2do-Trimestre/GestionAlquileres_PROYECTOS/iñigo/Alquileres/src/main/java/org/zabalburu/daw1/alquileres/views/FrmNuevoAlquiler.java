    private void cargarClientes() {
        for(Cliente c : service.getClientes()
                .stream()
                .sorted((c1,c2) -> c1.nombre().compareToIgnoreCase(c2.nombre()))
                .toList()){
            cbxCliente.addItem(c);
        }
    }
    
