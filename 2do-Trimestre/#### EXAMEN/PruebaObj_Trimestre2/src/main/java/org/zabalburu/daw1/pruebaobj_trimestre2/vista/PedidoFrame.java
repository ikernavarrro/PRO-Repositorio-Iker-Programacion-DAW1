    private void btnCrearPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearPedidoActionPerformed
        Pedido p = new Pedido();
        Cliente cliente = (Cliente) cbxCliente.getSelectedItem();
        if (txtDescripcion.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "La descripción es OBLIGATORIA!", "Error", JOptionPane.ERROR_MESSAGE);
            txtDescripcion.requestFocus();
        } else {
            try {
                p.setDescripcion(txtDescripcion.getText());
                if (Long.valueOf(ftxCantidad.getText()) > 0) { // Solo si la cantidad es superior a 0
                    p.setCantidad(Long.valueOf(ftxCantidad.getText()));
                } else {
                    JOptionPane.showMessageDialog(this, "Cantidad NO válida, Se ha establecido el mínimo por defecto!", "Error", JOptionPane.ERROR_MESSAGE);
                    p.setCantidad(1); // Por defecto 1
                }
                if (Double.valueOf(ftxPrecioUnidad.getText()) > 0) {// Solo si el precio es superior a 0
                    p.setPrecioUnitario(Double.valueOf(ftxPrecioUnidad.getText()));
                } else {
                    JOptionPane.showMessageDialog(this, "Precio NO válido, Se ha establecido el mínimo por defecto!", "Error", JOptionPane.ERROR_MESSAGE);
                    p.setPrecioUnitario(0.01); // Por defecto 0.01
                }
                try {
                    p.setFechaPedido(DateConversion.fromDate((Date) ftxFechaPedido.getValue()));
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Formato de la Fecha introducido NO válido, Se ha establecido la fecha de Hoy por defecto!", "Error", JOptionPane.ERROR_MESSAGE);
                    p.setFechaPedido(LocalDate.now());
                }
                p.setCliente((Cliente) cbxCliente.getSelectedItem());
                p.setPagado(ckbPagado.isSelected());
                servicio.addPedido(p);
                JOptionPane.showMessageDialog(this, "Pedido añadido con Éxito!");
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Vaya... Algo ha fallado y NO se ha añadido el Pedido", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnCrearPedidoActionPerformed

