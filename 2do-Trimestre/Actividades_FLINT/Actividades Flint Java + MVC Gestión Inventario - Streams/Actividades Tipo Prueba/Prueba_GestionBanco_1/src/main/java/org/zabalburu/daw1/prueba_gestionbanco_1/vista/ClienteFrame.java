    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Cliente c = new Cliente();
        if (estado == Estado.MODIFICACION) {
            c.setId(clientes.get(pos).getId());
            c.setCuentasBancarias(clientes.get(pos).getCuentasBancarias());
        }
        if (txtNombre.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "El Nombre es OBLIGATORIO!", "Error", JOptionPane.ERROR_MESSAGE);
            txtNombre.requestFocus();
        } else if (txtEmail.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "El Email es OBLIGATORIO!", "Error", JOptionPane.ERROR_MESSAGE);
            txtEmail.requestFocus();
        } else if (ftxNroTelefono.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "El Nro de Telefono es OBLIGATORIO!", "Error", JOptionPane.ERROR_MESSAGE);
            ftxNroTelefono.requestFocus();
        } else if (ftxFechaNacimiento.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "La Fecha de Nacimiento es OBLIGATORIA!", "Error", JOptionPane.ERROR_MESSAGE);
            ftxFechaNacimiento.requestFocus();
        } else if (ftxFechaAlta.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "La Fecha de Alta es OBLIGATORIA!", "Error", JOptionPane.ERROR_MESSAGE);
            ftxFechaAlta.requestFocus();
        } else {
            c.setNombre(txtNombre.getText());
            c.setEmail(txtEmail.getText());
            c.setNroTelefono(ftxNroTelefono.getText());
            Date d = (Date) ftxFechaNacimiento.getValue();
            try {
                c.setFechaNacimiento(DateConversion.fromDate(d));
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Formato de la Fecha de nacimiento introducido NO válido, restablecido a su valor anterior!", "Error", JOptionPane.ERROR_MESSAGE);
            }
            d = (Date) ftxFechaAlta.getValue();
            try {
                c.setFechaAlta(DateConversion.fromDate(d));
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Formato de la Fecha de alta introducido NO válido, restablecido a su valor anterior!", "Error", JOptionPane.ERROR_MESSAGE);
            }
            if (estado == Estado.ALTA) {
                servicio.addCliente(c);
            } else {
                servicio.modifyCliente(c);
            }
            clientes = servicio.getClientes();
            pos = clientes.indexOf(c);
            estado = Estado.CONSULTA;
            mostrar();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

