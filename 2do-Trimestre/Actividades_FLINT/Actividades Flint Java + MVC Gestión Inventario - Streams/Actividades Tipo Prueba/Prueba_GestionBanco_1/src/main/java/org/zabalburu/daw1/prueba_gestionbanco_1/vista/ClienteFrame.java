    private void mostrar() {
        if (clientes.isEmpty()) {
            estado = Estado.ALTA;
            pos = 0;
        }
        if (estado == Estado.ALTA) {
            lblContadorPos.setText("Nuevo");
            txtId.setText(" | Automático | ");
            ftxSaldoTotal.setText("0 €");
            txtNombre.setText("");
            txtEmail.setText("");
            ftxNroTelefono.setText("");
            ftxFechaNacimiento.setText("1950-01-01");
            ftxFechaAlta.setText(LocalDate.now().toString());
        } else {
            Cliente c = clientes.get(pos);
            txtId.setText(String.valueOf(c.getId()));
            ftxSaldoTotal.setText(nfMoneda.format(servicio.getSaldoTotalCliente(c.getId())));
            txtNombre.setText(c.getNombre());
            txtEmail.setText(c.getEmail());
            ftxNroTelefono.setText(c.getNroTelefono());
            ftxFechaNacimiento.setValue(DateConversion.fromLocalDate(c.getFechaNacimiento()));
            ftxFechaAlta.setValue(DateConversion.fromLocalDate(c.getFechaAlta()));
            lblContadorPos.setText("%02d / %02d".formatted(pos + 1, clientes.size()));
        }
        btnAnterior.setEnabled(pos > 0 && estado == Estado.CONSULTA);
        btnPrimero.setEnabled(pos > 0 && estado == Estado.CONSULTA);
        btnSiguiente.setEnabled(pos < clientes.size() - 1 && estado == Estado.CONSULTA);
        btnUltimo.setEnabled(pos < clientes.size() - 1 && estado == Estado.CONSULTA);
        btnCancelar.setEnabled(estado != Estado.CONSULTA && clientes.size() > 0);
        btnGuardar.setEnabled(estado != Estado.CONSULTA);
        btnModificar.setEnabled(estado == Estado.CONSULTA);
        btnNuevo.setEnabled(estado == Estado.CONSULTA);
        btnEliminar.setEnabled(estado == Estado.CONSULTA);
        btnNuevaCuenta.setEnabled(estado == Estado.CONSULTA);
        btnEliminarCuenta.setEnabled(estado == Estado.CONSULTA);
        txtId.setEnabled(false);
        ftxSaldoTotal.setEnabled(false);
        txtNombre.setEnabled(estado != Estado.CONSULTA);
        txtEmail.setEnabled(estado != Estado.CONSULTA);
        ftxNroTelefono.setEnabled(estado != Estado.CONSULTA);
        ftxFechaNacimiento.setEnabled(estado != Estado.CONSULTA);
        ftxFechaAlta.setEnabled(estado != Estado.CONSULTA);
        if (estado == Estado.ALTA) {
            DefaultTableModel dtmModelo = (DefaultTableModel) tblCuentasBancarias.getModel();
            dtmModelo.setDataVector(null, vctTitulosTabla);
        } else {
            cargarCuentasBancarias();
        }
    }

    private void cargarCuentasBancarias() {
        Vector<Vector<String>> vctDatos = new Vector<>();
        for (CuentaBancaria c : clientes.get(pos).getCuentasBancarias()) {
            Vector<String> vctFilaTabla = new Vector<>();
            vctFilaTabla.add(c.getId().toString());
            vctFilaTabla.add(c.getNroCuenta());
            vctFilaTabla.add(c.getTipo().toString());
            vctFilaTabla.add(c.getFechaApertura().toString());
            vctFilaTabla.add(c.getTitular().toString());
            vctFilaTabla.add(nfMoneda.format(c.getSaldo()));
            vctDatos.add(vctFilaTabla);
        }
        DefaultTableModel dtmModelo = (DefaultTableModel) tblCuentasBancarias.getModel();
        dtmModelo.setDataVector(vctDatos, vctTitulosTabla);
    }
