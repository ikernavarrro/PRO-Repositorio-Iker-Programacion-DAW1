    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Eliminar el Almacén: { %s [ ID: %d ]}".formatted(clientes.get(pos).getNombre(), clientes.get(pos).getId()), "Eliminar", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            try {
                servicio.removeCliente(clientes.get(pos).getId());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "No ha sido posible eliminar el Cliente!", "Error", JOptionPane.ERROR_MESSAGE);
            }
            clientes = servicio.getClientes();
            if (pos == clientes.size()) {
                pos--;
            }
            mostrar();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

