    private void btnEliminarCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCuentaActionPerformed
        int filaSeleccionada = tblCuentasBancarias.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this,
                    "Debe seleccionar la Cuenta que desee ELIMINAR",
                    "Error",
                    JOptionPane.WARNING_MESSAGE);
        } else {
            CuentaBancaria eliminar = clientes.get(pos).getCuentasBancarias().get(filaSeleccionada);
            servicio.removeCuentaBancaria(eliminar.getId());
            clientes = servicio.getClientes();
            mostrar();
        }
    }//GEN-LAST:event_btnEliminarCuentaActionPerformed

