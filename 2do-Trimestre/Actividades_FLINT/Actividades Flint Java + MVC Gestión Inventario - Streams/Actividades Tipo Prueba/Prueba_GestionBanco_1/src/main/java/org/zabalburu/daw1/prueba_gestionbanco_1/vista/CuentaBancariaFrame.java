    private void cargarClientes() {
        for (Cliente c : servicio.getClientes()) {
            cbxTitular.addItem(c);
        }
        cbxTitular.setSelectedItem(cliente);
    }

    private void cargarTipoCuenta() {
        cbxTipoCuenta.addItem(TipoCuentaBancaria.JOVEN);
        cbxTipoCuenta.addItem(TipoCuentaBancaria.AHORROS);
        cbxTipoCuenta.addItem(TipoCuentaBancaria.CORRIENTE);
        cbxTipoCuenta.addItem(TipoCuentaBancaria.INVERSIONES);
        cbxTipoCuenta.setSelectedItem(TipoCuentaBancaria.JOVEN);
    }
