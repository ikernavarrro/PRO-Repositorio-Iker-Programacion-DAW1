    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        CuentaBancaria cuenta = new CuentaBancaria();
        Cliente cliente = (Cliente) cbxTitular.getSelectedItem();
        if (txtNroCuenta.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "El Número de Cuenta es OBLIGATORIO!", "Error", JOptionPane.ERROR_MESSAGE);
            txtNroCuenta.requestFocus();
        } else {
            try {
                cuenta.setSaldo(Double.valueOf(ftxSaldo.getText()));
                cuenta.setNroCuenta(txtNroCuenta.getText());
                cuenta.setTitular((Cliente) cbxTitular.getSelectedItem());
                try {
                    cuenta.setFechaApertura(DateConversion.fromDate((Date)ftxFechaApertura.getValue()));
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Formato de la Fecha de apertura introducido NO válido, restablecido a su valor por defecto!", "Error", JOptionPane.ERROR_MESSAGE);
                }
                cuenta.setTipo((TipoCuentaBancaria)cbxTipoCuenta.getSelectedItem());
                servicio.addCuentaBancaria(cuenta);
                JOptionPane.showMessageDialog(this, "Cuenta creada con Éxito!");
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Vaya... Algo ha fallado y NO se ha creado la Cuenta", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAñadirActionPerformed

